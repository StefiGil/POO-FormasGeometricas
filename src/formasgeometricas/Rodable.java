package formasgeometricas;

// Interfaz que define el comportamiento de los objetos que pueden rodar.
// Cualquier clase que implemente esta interfaz deberá proporcionar
// una implementación del métdo 'rodar()', permitiendo así que
// los objetos de esas clases puedan realizar la acción de rodar.

public interface Rodable {

    void rodar();
}
